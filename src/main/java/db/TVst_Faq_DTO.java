package db;

public class TVst_Faq_DTO {
	
	private int row_num;
	private String faq_code;
	private String faq_date;
	private String faq_cate;
	private String faq_cate_name;
	private String faq_title;
	private String faq_content;
	
	public int getRow_num() {
		return row_num;
	}
	public void setRow_num(int row_num) {
		this.row_num = row_num;
	}
	public String getFaq_code() {
		return faq_code;
	}
	public void setFaq_code(String faq_code) {
		this.faq_code = faq_code;
	}
	public String getFaq_date() {
		return faq_date;
	}
	public void setFaq_date(String faq_date) {
		this.faq_date = faq_date;
	}
	public String getFaq_cate() {
		return faq_cate;
	}
	public void setFaq_cate(String faq_cate) {
		this.faq_cate = faq_cate;
	}
	public String getFaq_cate_name() {
		return faq_cate_name;
	}
	public void setFaq_cate_name(String faq_cate_name) {
		this.faq_cate_name = faq_cate_name;
	}
	public String getFaq_title() {
		return faq_title;
	}
	public void setFaq_title(String faq_title) {
		this.faq_title = faq_title;
	}
	public String getFaq_content() {
		return faq_content;
	}
	public void setFaq_content(String faq_content) {
		this.faq_content = faq_content;
	}

}
